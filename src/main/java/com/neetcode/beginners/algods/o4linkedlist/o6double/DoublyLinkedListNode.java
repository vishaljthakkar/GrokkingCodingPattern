package com.neetcode.beginners.algods.o4linkedlist.o6double;

public class DoublyLinkedListNode {

    public int val;
    public DoublyLinkedListNode next;
    public DoublyLinkedListNode prev;

    public DoublyLinkedListNode(int val) {
        this.val = val;
        this.next = null;
        this.prev = null;
    }
}
